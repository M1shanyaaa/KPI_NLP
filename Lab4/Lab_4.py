def fetch_wiki(wiki_key: str) -> str:
    try:
        r = requests.get(
            f"https://uk.wikipedia.org/api/rest_v1/page/summary/{wiki_key}",
            timeout=4,
            headers={"User-Agent": "lviv-bot/1.0 (student project)"},
        )
        if r.status_code == 200:
            return r.json().get("extract", "")[:300]
    except Exception:
        pass
    return ""


